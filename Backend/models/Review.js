import mongoose from 'mongoose';

const reviewSchema = new mongoose.Schema({
  productId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Product',
    required: true
  },
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: false // üë§ Tu pourras le rendre obligatoire si tu relies aux utilisateurs connect√©s
  },
  name: {
    type: String,
    required: true,
    trim: true
  },
  rating: {
    type: Number,
    required: true,
    min: 1,
    max: 5
  },
  comment: {
    type: String,
    required: true
  }
}, {
  timestamps: true // ‚è±Ô∏è G√®re createdAt / updatedAt automatiquement
});

const Review = mongoose.model('Review', reviewSchema);

export default Review;
